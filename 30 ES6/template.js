let firstName = 'Rahim';

// Double quotes
let lastName = "Khan";

// Backticks (template literals)
let fullName = `${firstName} ${lastName}`;

// Forward slash (/)
let division = 10 / 2;

// Backslash (\) for escaping characters
let message = "He said \"Hello\"";
let path = "C:\\Users\\Rahim\\Documents";

// Semicolon (;)
let age = 25;

console.log(fullName);
